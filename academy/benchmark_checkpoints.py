"""Benchmarks a list of trained checkpoints and writes a comparison table.

Every trial runs through the exact same path as ``python infer.py --checkpoint
...`` — same accuracy metrics (``infer.py::run_segmentation_eval``), same FPS
benchmark (``infer.py::benchmark_fps``) — via each backend's own library
(``ultralytics``/``rfdetr``) through the ``ModelAdapter`` interface. It never
touches ``onnxruntime``: these are raw, unoptimized PyTorch checkpoints
straight off training, not the ``optimize.py`` ONNX pipeline.

Usage:
    python benchmark_checkpoints.py --config config.yaml --trials benchmark_trials.yaml
"""
from __future__ import annotations

import argparse
import dataclasses
import datetime
import gc
from pathlib import Path

import cv2

from infer import _list_images, benchmark_fps, run_segmentation_eval
from utils.benchmark.trials import Trial, load_trials
from utils.config import Config
from utils.models import get_adapter

GPU_LABEL = "RTX-3060-Ti"


def _cfg_for_trial(base_cfg: Config, trial: Trial) -> Config:
    model = dataclasses.replace(base_cfg.model, checkpoint=trial.checkpoint)
    if trial.framework == "yolo":
        model = dataclasses.replace(
            model, yolo=dataclasses.replace(model.yolo, variant=trial.variant)
        )
    else:
        model = dataclasses.replace(
            model, rfdetr=dataclasses.replace(model.rfdetr, variant=trial.variant)
        )
    return dataclasses.replace(base_cfg, framework=trial.framework, model=model)


def _mean_across_classes(summary) -> dict[str, float]:
    """Collapse the per-class mean/std table from run_segmentation_eval into
    one mean per metric — fine for the single-class ``cloud`` dataset this
    pipeline targets, and a reasonable unweighted summary if that changes.
    """
    means = summary.xs("mean", axis=1, level=1).mean()
    return {k: float(v) for k, v in means.items()}


def run_trial(base_cfg: Config, trial: Trial) -> dict:
    cfg = _cfg_for_trial(base_cfg, trial)
    print(f"\n=== {trial.name} ({trial.framework}/{trial.variant}) — {trial.checkpoint} ===")

    adapter = get_adapter(cfg)
    out_dir = Path(cfg.output_dir) / cfg.framework / trial.variant / "test_eval"
    out_dir.mkdir(parents=True, exist_ok=True)

    summary = run_segmentation_eval(cfg, adapter, out_dir)
    metrics = _mean_across_classes(summary)

    sample_images = _list_images(cfg.dataset.images_dir(cfg.dataset.test_split))
    if not sample_images:
        raise FileNotFoundError(f"no test images under {cfg.dataset.images_dir(cfg.dataset.test_split)}")
    sample = cv2.imread(str(sample_images[0]))
    result, peak_vram_mb = benchmark_fps(cfg, adapter, sample)

    row = {
        "name": trial.name,
        **metrics,
        "fps": result.fps,
        "vram_mb": peak_vram_mb if peak_vram_mb is not None else result.vram_mb,
    }

    del adapter
    gc.collect()
    try:
        import torch

        if torch.cuda.is_available():
            torch.cuda.empty_cache()
    except ImportError:
        pass

    return row


def render_summary_md(rows: list[dict]) -> str:
    header = [
        "Model", "IoU", "Dice", "Precision", "Recall", "Accuracy",
        f"FPS ({GPU_LABEL})", "Peak VRAM (MB)",
    ]
    lines = [
        "# Summary",
        "",
        f"Finetuned from pretrained default models using a {GPU_LABEL}.",
        "",
        "**Methodology:** generated by `benchmark_checkpoints.py`, which drives "
        "each checkpoint straight through `infer.py`'s evaluation/benchmark path "
        "— i.e. each backend's own library (`ultralytics` for YOLO, `rfdetr` for "
        "RF-DETR) through the `ModelAdapter` interface, **not** the ONNX Runtime "
        "backend from `optimize.py`. None of these checkpoints are "
        "exported/optimized (no fp16/int8/pruning) — this is the raw PyTorch "
        "checkpoint straight off training. VRAM is peak CUDA memory allocated "
        "during the FPS benchmark (`torch.cuda.max_memory_allocated`).",
        "",
        f"Generated: {datetime.date.today().isoformat()}",
        "",
        f"| {' | '.join(header)} |",
        f"| {' | '.join(['---'] * len(header))} |",
    ]
    for r in rows:
        vram = f"{r['vram_mb']:.0f}" if r.get("vram_mb") is not None else "n/a"
        lines.append(
            f"| {r['name']} | {r['iou']:.3f} | {r['dice']:.3f} | {r['precision']:.3f} | "
            f"{r['recall']:.3f} | {r['accuracy']:.3f} | {r['fps']:.1f} | {vram} |"
        )
    return "\n".join(lines) + "\n"


def main(config_path: str, trials_path: str, output_md: str) -> None:
    base_cfg = Config.from_yaml(config_path)
    trials = load_trials(trials_path)

    rows = []
    for trial in trials:
        try:
            rows.append(run_trial(base_cfg, trial))
        except Exception as e:
            print(f"!! {trial.name} failed: {e}")

    Path(output_md).write_text(render_summary_md(rows), encoding="utf-8")
    print(f"\nWrote {len(rows)}/{len(trials)} rows to {output_md}")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="Benchmark a list of trained checkpoints (raw framework libraries) and write SUMMARY.md"
    )
    parser.add_argument("--config", default="config.yaml")
    parser.add_argument("--trials", default="benchmark_trials.yaml")
    parser.add_argument("--output", default="SUMMARY.md")
    args = parser.parse_args()
    main(args.config, args.trials, args.output)
